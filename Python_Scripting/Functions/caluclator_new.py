num1 = 10
num2 = 5

def addition():
    add = num1 + num2
    print(add)

def sub():
    sub = num1 - num2
    print(sub)

def mul():
    mul = num1 * num2
    print(mul)

# You have to invoke or call this function
addition()
sub()
mul()

